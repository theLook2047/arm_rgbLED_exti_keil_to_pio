void EXTI1_IRQHandler()
{
	if(EXTI->PR & EXTI_Line1) {
		GPIOC->ODR ^= GPIO_Pin_10;
		EXTI->PR |= EXTI_Line1;
	}
}

void EXTI4_IRQHandler()
{
	if(EXTI->PR & EXTI_Line4) {
		GPIOC->ODR ^= GPIO_Pin_12;
		EXTI->PR |= EXTI_Line4;
	}
}

void EXTI0_IRQHandler()
{
	if(EXTI->PR & EXTI_Line0) {
		GPIOC->ODR ^= GPIO_Pin_11;
		EXTI->PR |= EXTI_Line0;
	}
}
